public class For_Loop_Quize {
    public static void main(String[] args) {
        // 2i= even number= 0 2 4 6 8 10
        //2i+1= odd number= 1  3 5 7 9

               // FOR EVEN NUMBER

        int n =5;
        for (int i=0; i<n; i++){
            System.out.println(2*i+1);

               // FOR ODD NUMBER
            System.out.println(2*i);
        }
    }
}
